function r_equation_Callback(hObject, eventdata, handles)
% hObject    handle to r_equation (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)

% Hint: get(hObject,'Value') returns toggle state of r_equation


% --- Executes on button press in enter.
function enter_Callback(hObject, eventdata, handles)
% hObject    handle to enter (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
val = get(handles.group,'SelectedObject');
oval = get(val,'String');
global a;
global b;
c = get(handles.equation,'string');
d = get(handles.edit17,'string');
d = str2double(strsplit(d,' '));
switch (oval)
    case 'X axis'
        axes(handles.axes1);
        set(handles.error2,'String',' ');
        plot(a)
    case 'Y axis'
        axes(handles.axes1);
        set(handles.error2,'String',' ');
         plot(b)
    case 'XY axis'
        axes(handles.axes1);
        set(handles.error2,'String',' ');
         plot(a,b);
    case 'resultant'
        axes(handles.axes1);
        set(handles.error2,'String',' ');
         plot(d);
    case 'Sin'
        x= 0:0.1:10;
        axes(handles.axes1);
        set(handles.error2,'String',' ');
        plot(sin(x))
    case 'Cosine'
        x= 0:0.1:10;
        axes(handles.axes1);
        set(handles.error2,'String',' ');
        plot(cos(x))
    case 'Equation'
        x = 0:0.1:10;
        y = 0:0.1:10;
        set(handles.error2,'String',' ');
        axes(handles.axes1);
        try 
            plot(eval(c))
            set(handles.error2,'String',' ');
        catch 
            set(handles.error2,'String','Equation field is empty');
        end
end


